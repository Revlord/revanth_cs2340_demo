public class CounterModel {
    private int counter;

    public CounterModel() {
        this.counter = 0;
    }

    //gerCounter() method to returns counter value
    public int getCounter() {
        return counter;
    }

    //incrementCounter() method that increments counter by 1
    public void incrementCounter() {
        counter++;
    }



}
